#ifndef _BOARD_H_
#define _BOARD_H_

#include "stm32f0xx.h"

#define	_BV(bit) (1<<(bit))

/* =====RESERVED ON BOARD===== */
/*
 PA0 - BUTTON1
 PA13 - SWD
 PA14 - SWD
 PC8 - BLUE LED 
 PC9 - GREEN LED
 PF0 - XTAL
 PF1 - XTAL
*/ 

/* ======QVGA 2.2 TFT SPI====== */
#define ILI9341_CS_PORT    GPIOC
#define ILI9341_CS_PIN     GPIO_Pin_5
#define ILI9341_DC_PORT    GPIOC
#define ILI9341_DC_PIN     GPIO_Pin_4
#define ILI9341_RST_PORT   GPIOA
#define ILI9341_RST_PIN    GPIO_Pin_6
#define ILI9341_MOSI_PORT  GPIOA 
#define ILI9341_MOSI_PIN   GPIO_Pin_7
#define ILI9341_SCK_PORT   GPIOA 
#define ILI9341_SCK_PIN    GPIO_Pin_5
#define ILI9341_CS_SET     (ILI9341_CS_PORT->BSRR   = ILI9341_CS_PIN)
#define ILI9341_CS_RESET   (ILI9341_CS_PORT->BRR    = ILI9341_CS_PIN)
#define ILI9341_DC_SET     (ILI9341_DC_PORT->BSRR  = ILI9341_DC_PIN)
#define ILI9341_DC_RESET   (ILI9341_DC_PORT->BRR   = ILI9341_DC_PIN)
#define ILI9341_RST_SET    (ILI9341_RST_PORT->BSRR  = ILI9341_RST_PIN)
#define ILI9341_RST_RESET  (ILI9341_RST_PORT->BRR   = ILI9341_RST_PIN)
#define ILI9341_MOSI_SET   (ILI9341_MOSI_PORT->BSRR = ILI9341_MOSI_PIN)
#define ILI9341_MOSI_RESET (ILI9341_MOSI_PORT->BRR  = ILI9341_MOSI_PIN)
#define ILI9341_SCK_SET    (ILI9341_SCK_PORT->BSRR  = ILI9341_SCK_PIN)
#define ILI9341_SCK_RESET  (ILI9341_SCK_PORT->BRR   = ILI9341_SCK_PIN)
/* ======END QVGA 2.2 TFT SPI====== */

// USART - digital I/O
#define USART_PORT    GPIOA
#define USART_TX_PIN  GPIO_Pin_9
#define USART_RX_PIN  GPIO_Pin_10

// BTN1 - digital INPUT
#define BTN_PORT    GPIOA
#define BTN1_PIN    GPIO_Pin_0
#define BTN1_STATE  (BTN_PORT->IDR & BTN1_PIN)

#endif